# Operadores matemáticos

# +
# -
# *
# /

print("adição - ", 10 + 10)
print("subtração - ", 10 - 10)
print("multiplicação - ", 10 * 10)
print("divisão - ", 10 / 10)


# // - resultado arredondado da divisão para um numero inteiro
# ** - exponencial
# % - resto da divisão entre dois numeros
# () - operador de precedencia

print("arredondado - ", 10 // 10)
print("exponencial - ", 10 ** 10)
print("resto da divisão - ", 10 % 10)
print("operador de precedencia - ", (10 - 5) + 10)
