# Manipulando strings 
# * string indices 
# * Funções buit-in len, abs, type, print, etc..

texto = 'Python 3'
# acessando indices positivos da string
print(texto[2])

text2 = 'Python 2'
# acessando indices negativos da string
print(text2[-1])
# recortando carater
print(text2[:-1])
print(text2[-9:-5])